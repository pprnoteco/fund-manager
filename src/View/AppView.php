namespace App\View;

use Cake\View\View;

class AppView extends View
{
    /*
     |--------------------------------------------------------------------
     | Initialize
     |--------------------------------------------------------------------
     */
    
    public function initialize()
    {
        parent::initialize();
        $this->loadHelper('Auth');
    }
    
    /*
     |--------------------------------------------------------------------
     | Url
     |--------------------------------------------------------------------
     */
    
    public function url($url = null, $full = false)
    {
        return $this->Url->build($url, $full);
    }
}
