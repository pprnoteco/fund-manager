/*
 |------------------------------------------------------------------------
 | App: Toolbar
 |------------------------------------------------------------------------
 */

(function(App){
    
    var View = App.View;
    
    /*
     |--------------------------------------------------------------------
     | Constructor
     |--------------------------------------------------------------------
     */
    
    var Toolbar = View.extend({
        
        reset: function (model) {
            this.model = model;
            this.render();
        }
        
    });
    
    App.Toolbar = Toolbar;
    
}(App));
