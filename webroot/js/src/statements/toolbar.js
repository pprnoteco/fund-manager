/*
 |------------------------------------------------------------------------
 | App\Statements: Toolbar
 |------------------------------------------------------------------------
 */

(function(App){

    /*
     |--------------------------------------------------------------------
     | Constructor
     |--------------------------------------------------------------------
     */
    
    var Toolbar = App.Toolbar.extend({
     
        /*
         |----------------------------------------------------------------
         | Constructor
         |----------------------------------------------------------------
         */
        
        initialize: function () {
            var $elem = this.$el;
            $download = $elem.find('[data-action="download"]');
            this.$download = $download;
        },
     
        /*
         |----------------------------------------------------------------
         | Render
         |----------------------------------------------------------------
         */
        
        render: function () {
            var value = !this.model;
            this.$download.prop('disabled', value);
        }
        
    });
    
    App.Statements.Toolbar = Toolbar;
    
}(App));
